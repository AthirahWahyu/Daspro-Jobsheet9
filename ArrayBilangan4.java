public class ArrayBilangan4 {
    public static void main(String[] args) {

        // Array bertipe integer dengan nama bil dengan kapasitas 4 elemen
        int [] bil = {5, 13, -7, 17};

        // Output
        for (int i = 0; i <= 4; i++) {
            System.out.println(bil[i]);
        }
    }
}

